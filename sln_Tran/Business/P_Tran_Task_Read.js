                var PersonName = BC_Tran_TaskMXBase.getRowFieldValue(i, "PersonName");
                var OrgName = BC_Tran_TaskMXBase.getRowFieldValue(i, "OrgName");
                var Division = BC_Tran_TaskMXBase.getRowFieldValue(i, "Division");
